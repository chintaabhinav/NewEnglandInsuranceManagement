    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
         int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a policy.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Get selected policy details
        String policyName = jTable1.getValueAt(selectedRow, 0).toString();
        String claimPercentage = jTable1.getValueAt(selectedRow, 1).toString();
        String policyAmount = jTable1.getValueAt(selectedRow, 2).toString();
        String premiumAmount = jTable1.getValueAt(selectedRow, 3).toString();
        String coveragePeriod = jTable1.getValueAt(selectedRow, 4).toString();
        String deductibleAmount = jTable1.getValueAt(selectedRow, 5).toString();

        // Pass data to BillingPagee
        BillingPagee billingPage = new BillingPagee(
            workPanel, customer, vehicleType, year, brand, model, vin,
            policyName, claimPercentage, policyAmount, premiumAmount, coveragePeriod, deductibleAmount
        );
        workPanel.add("BillingPagee", billingPage);

        // Navigate to BillingPagee
        CardLayout layout = (CardLayout) workPanel.getLayout();
        layout.show(workPanel, "BillingPagee");
    }//GEN-LAST:event_btnNextActionPerformed


