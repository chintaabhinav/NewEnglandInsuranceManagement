     private void populatePoliciesTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        String query = "SELECT * FROM policies";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(query);
             ResultSet rs = stmt.executeQuery()) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("policy_name"),
                    rs.getString("claim_percentage"),
                    rs.getString("policy_amount"),
                    rs.getString("premium_amount"),
                    rs.getString("coverage_period"),
                    rs.getString("deductible_amount")
                });
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error fetching policy data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

